; CPC_ENABLEFIRMWARE
; Restores the callback in the jumpblock used by the firmware
; each time an interrupt happens. Color and screen settings can
; be overwritten by Firmware values as soon as it gets enabled again.
; Inputs:
;     None
; Outputs:
;	  None
;     HL gets modified.
cpc_EnableFirmware:
	di
	ld      hl,(_rslib_backup_fw)
	ld 		(&0038),hl
	ei
	ret