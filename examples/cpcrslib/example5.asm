; EXAMPLE 004 - Music, SFX & Keyboard

org &4000

.main
    xor     a
    inc     a
    call    cpc_SetModeFW
    ld      hl,_texto00
    call    cpc_PrintStrFW
    ld      hl,_texto01
    call    cpc_PrintStrFW
    ld      hl,_texto02
    call    cpc_PrintStrFW
    ld      hl,_texto03
    call    cpc_PrintStrFW

    call    cpc_DisableFirmware

    ; Start playing first song
    ld      de,_texto10
    ld      l,0     ; X
    ld      h,10*8  ; Y
    call    cpc_DrawStrXY_M1
    call    change_song

    ; Cursor keys are in the key assignment table by default
    ; lets add the 1-4 numbers to fire the sound effects:
    ld      bc,KEY_1
    ld      e,5
    call    cpc_AssignKey
    ld      bc,KEY_2
    ld      e,6
    call    cpc_AssignKey
    ld      bc,KEY_3
    ld      e,7
    call    cpc_AssignKey
    ld      bc,KEY_4
    ld      e,8
    call    cpc_AssignKey

__main_loop:
    ; leave sometime for the last key to take effect, as it
    ; affects interrupts
    call    pause
    call    cpc_WyzTestPlayer
    ; cpc_ScanKeyboard + cpc_CheckKey is faster than cpc_TestKey
    ; when we want to test several keys.
    call    cpc_ScanKeyboard
    __test_cursor_up:
    ld      l,2
    call    cpc_CheckKey
    ld      a,h
    or      l
    jr      z,__test_cursor_down
    ld      a,(_current_song)
    inc     a
    cp      5
    jr      nc,__test_key1
    ld      (_current_song),a
    call    cpc_WyzSetPlayerOff
    call    pause
    call    change_song
    jr      __test_key1
    
    __test_cursor_down:
    ld      l,3
    call    cpc_CheckKey
    ld      a,h
    or      l
    jr      z,__test_key1
    ld      a,(_current_song)
    dec     a
    jp      m,__test_key1
    ld      (_current_song),a
    call    cpc_WyzSetPlayerOff
    call    pause
    call    change_song

    __test_key1:
    ld      l,5
    call    cpc_CheckKey
    ld      a,h
    or      l
    jr      z,__test_key2
    ld      bc,0 ; channel and effect number
    call    cpc_WyzStartEffect
    jr      __test_key_end

    __test_key2:
    ld      l,6
    call    cpc_CheckKey
    ld      a,h
    or      l
    jr      z,__test_key3
    ld      bc,&0101 ; channel and effect number
    call    cpc_WyzStartEffect
    jr      __test_key_end
  
    __test_key3:
    ld      l,7
    call    cpc_CheckKey
    ld      a,h
    or      l
    jr      z,__test_key4
    ld      bc,&0202 ; channel and effect number
    call    cpc_WyzStartEffect
    jr      __test_key_end

    __test_key4:
    ld      l,8
    call    cpc_CheckKey
    ld      a,h
    or      l
    jr      z,__test_key_end
    ld      bc,&0301 ; channel and effect number
    call    cpc_WyzStartEffect
    __test_key_end:
    jp __main_loop

change_song:
	ld      a,(_current_song)
__current_song0:
    cp      0
    jr      nz,__current_song1
    ld      hl,_SONG_TABLE_0
    push    hl
    ld      hl,_EFFECT_TABLE
    push    hl
    ld      hl,_RULE_TABLE_0
    push    hl
    ld      hl,_SOUND_TABLE_0
    push    hl
    ld      de,_texto05
    ld      l,0     ; X
    ld      h,11*8  ; Y
    call    cpc_DrawStrXY_M1
    jr      __play_song
__current_song1:
    cp      1
    jr      nz,__current_song2
    ld      hl,_SONG_TABLE_1
    push    hl
    ld      hl,_EFFECT_TABLE
    push    hl
    ld      hl,_RULE_TABLE_1
    push    hl
    ld      hl,_SOUND_TABLE_1
    push    hl
    ld      de,_texto06
    ld      l,0     ; X
    ld      h,11*8  ; Y
    call    cpc_DrawStrXY_M1
    jr      __play_song
__current_song2:
    cp      2
    jr      nz,__current_song3
    ld      hl,_SONG_TABLE_2
    push    hl
    ld      hl,_EFFECT_TABLE
    push    hl
    ld      hl,_RULE_TABLE_2
    push    hl
    ld      hl,_SOUND_TABLE_2
    push    hl
    ld      de,_texto07
    ld      l,0     ; X
    ld      h,11*8  ; Y
    call    cpc_DrawStrXY_M1
    jr      __play_song
__current_song3:
    cp      3
    jr      nz,__current_song4
    ld      hl,_SONG_TABLE_3
    push    hl
    ld      hl,_EFFECT_TABLE
    push    hl
    ld      hl,_RULE_TABLE_3
    push    hl
    ld      hl,_SOUND_TABLE_3
    push    hl
    ld      de,_texto08
    ld      l,0     ; X
    ld      h,11*8  ; Y
    call    cpc_DrawStrXY_M1
    jr      __play_song
__current_song4:
    cp      4
    ret     nz
    ld      hl,_SONG_TABLE_4
    push    hl
    ld      hl,_EFFECT_TABLE
    push    hl
    ld      hl,_RULE_TABLE_4
    push    hl
    ld      hl,_SOUND_TABLE_4
    push    hl
    ld      de,_texto09
    ld      l,0     ; X
    ld      h,11*8  ; Y
    call    cpc_DrawStrXY_M1
__play_song:
    call    cpc_WyzInitPlayer  ; Set addresses
    pop     hl
    pop     hl
    pop     hl
    pop     hl
    xor     a
    call    cpc_WyzLoadSong    ; Select song to play (uncompress it and the start to play)
	jp      cpc_WyzSetPlayerOn ; Start music and sound effects (SFX)


pause:
	ld      b,10
	pause_loop:
		halt
	djnz pause_loop
    ret

